#include "npp.h"


/* ======================================================================= */
/* =============================== SERVICES ============================== */
/* ======================================================================= */


/* --------------------------------------------------------------------------
   Service
-------------------------------------------------------------------------- */
void getCustomer(int ci)
{
    OUT_HTML_HEADER;
    OUT("<h1>%s</h1>", APP_WEBSITE);
    OUT("<p>Hello from getCustomer service!</p>");
    OUT_HTML_FOOTER;
}


/* --------------------------------------------------------------------------
   Service
-------------------------------------------------------------------------- */
void getAccounts(int ci)
{
    OUT_HTML_HEADER;
    OUT("<h1>%s</h1>", APP_WEBSITE);
    OUT("<p>Hello from getAccounts service!</p>");
    OUT_HTML_FOOTER;
}


/* ======================================================================= */
/* ========================== END OF SERVICES ============================ */
/* ======================================================================= */



/* --------------------------------------------------------------------------
   Entry point
-------------------------------------------------------------------------- */
void npp_svc_main(int ci)
{
    if ( SVC("getCustomer") )
        getCustomer(ci);
    else if ( SVC("getAccounts") )
        getAccounts(ci);
}


/* --------------------------------------------------------------------------
   Server start
   Return true if successful
-------------------------------------------------------------------------- */
bool npp_svc_init()
{
    return true;
}


/* --------------------------------------------------------------------------
   Server stop
-------------------------------------------------------------------------- */
void npp_svc_done()
{
}
